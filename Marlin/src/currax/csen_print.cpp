#include <stdlib.h>
#include <string.h>

#include "fcomMsg.h"
#include "fcomMsgPort.h"
#include "csenSettings.h"

#include "csen_print.h"

//******************************************************************************
//******************************************************************************
//******************************************************************************
// Send a print message to the fcom host.

void csen_print(char* const aString)
{
   // Guard.
   if (!CSen::gSettings.mPrintEnable) return;

   // Send a print message.
   FCom::PrintMsg tMsg;
   strncpy(&tMsg.mString[0],aString,FCom::PrintMsg::cMaxSize);
   FCom::gMsgPort.doSendMsg(&tMsg);
}

